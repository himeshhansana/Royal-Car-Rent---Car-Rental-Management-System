    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int row = jTable1.getSelectedRow();

            String nic = String.valueOf(jTable1.getValueAt(row, 0));
            jTextField1.setText(nic);
            String fname = String.valueOf(jTable1.getValueAt(row, 1));
            jTextField2.setText(fname);

            LocalDate currentDate = LocalDate.now();
            Month currentMonth = currentDate.getMonth();
            int currentMonthIndex = currentMonth.getValue();

            String[] monthNames = {
                "January", "February", "March", "April", "May", "June",
                "July", "August", "September", "October", "November", "December"
            };
            String monthName = monthNames[currentMonthIndex - 1];

            ResultSet rs = MySQL2.executeSearch("SELECT * FROM salary WHERE `employee_nic` = '" + nic + "' AND `month` = '" + monthName + "'");

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Already Paid", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                if (evt.getClickCount() == 2) {
                    nic = String.valueOf(jTable1.getValueAt(row, 0));
                    fname = String.valueOf(jTable1.getValueAt(row, 1));

                    Admin admin = (Admin) this.home.jPanel2.getComponent(0);
                    admin.setEmployee(nic, fname);
                    SwingUtilities.updateComponentTreeUI(this.home.jPanel2);

                    this.dispose();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

