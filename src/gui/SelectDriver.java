    private void loadDrivers(String nic, String name) {
        try {

            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `driver`"
                    + "INNER JOIN gender ON driver.gender_id = gender.id "
                    + "INNER JOIN availabilty ON driver.availabilty_id = availabilty.id "
                    + "WHERE availabilty.availabilty = 'Available'"
                    + "AND `nic` LIKE '%" + nic + "%' AND `first_name` LIKE '%" + name + "%'");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();

                vector.add(resultSet.getString("nic"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("gender.name"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
            Logger loggerFile = SplashScreen.getLoggerFile();
            loggerFile.log(Level.SEVERE, "loadDrivers", e);
        }

    }

