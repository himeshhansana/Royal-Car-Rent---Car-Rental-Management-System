    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        int row = jTable2.getSelectedRow();

        String nic = String.valueOf(jTable2.getValueAt(row, 0));
        jTextField1.setText(nic);

        String fname = String.valueOf(jTable2.getValueAt(row, 1));
        jTextField2.setText(fname);

        if (evt.getClickCount() == 2) {

            Rental rntl = (Rental) this.home.jPanel2.getComponent(0);
            rntl.setDriver(nic, fname);
            SwingUtilities.updateComponentTreeUI(this.home.jPanel2);

            this.dispose();
        }

    }//GEN-LAST:event_jTable2MouseClicked

