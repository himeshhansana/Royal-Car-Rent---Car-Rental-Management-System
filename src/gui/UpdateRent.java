    private void cost() {
        try {
            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `cost`");

            while (rs.next()) {
                String cost = rs.getString("cost_per_km");
                jLabel9.setText(cost);
            }
        } catch (Exception e) {
            e.printStackTrace();
            Logger loggerFile = SplashScreen.getLoggerFile();
            loggerFile.log(Level.SEVERE, "RentCost", e);
        }
    }

    private void DriverCost() {
        try {
            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `driver_cost`");

            while (rs.next()) {
                String DriverCost = rs.getString("cost");
                if (Driver == null) {
                    jTextField6.setText("0.00");
                } else {
                    jTextField6.setText(DriverCost);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            Logger loggerFile = SplashScreen.getLoggerFile();
            loggerFile.log(Level.SEVERE, "DriverCost", e);
        }
    }

