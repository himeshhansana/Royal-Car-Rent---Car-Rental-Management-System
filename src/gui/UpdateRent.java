    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased

        try {
            Double startMeter = Double.parseDouble(jTextField3.getText());
            Double endMeter = Double.parseDouble(jTextField4.getText());
            Double driverCostT = Double.parseDouble(jTextField6.getText());
            Double costValue = Double.parseDouble(jLabel9.getText());

            double result = endMeter - startMeter;
            jTextField5.setText(String.valueOf(result * costValue));

            double total = result * costValue + driverCostT;
            jTextField8.setText(String.valueOf(total));

        } catch (NumberFormatException e) {
            jTextField4.setText("");
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values in all meter and cost fields.", "Invalid Input", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_jTextField4KeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String rentalId = jTextField1.getText();
        String payment = jTextField5.getText();
        String CustomerNic = jTextField9.getText();
        String vehicleLicenNo = jTextField7.getText();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        Date endDate = jDateChooser1.getDate();
        SimpleDateFormat formatterend = new SimpleDateFormat("yyyy-MM-dd");
        try {

            if (Integer.parseInt(jTextField3.getText()) >= Integer.parseInt(jTextField4.getText())) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Milage", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                MySQL2.executeIUD("INSERT INTO `payment` (`payment_method_payment_method_id`,`payment_date`,`amount`,`customer_nic`,`vehicle_licene_no`,`rental_id`)"
                        + "VALUES('1','" + LocalDateTime.now() + "','" + payment + "','" + CustomerNic + "','" + vehicleLicenNo + "','" + rentalId + "')");

                MySQL2.executeIUD("UPDATE `rental` SET `end_date` = '" + formatter.format(jDateChooser1.getDate()) + "' , `end_meter` = '" + jTextField4.getText() + "'"
                        + "WHERE `id` = '" + rentalId + "'");

                if (!Driver.equals("null")) {
                    MySQL2.executeIUD("UPDATE `driver` SET `availabilty_id` = '1' WHERE `nic` = '" + Driver + "' ");

                }

                MySQL2.executeIUD("UPDATE `vehicle` SET `availabilty_id` = '1' WHERE `licene_no` = '" + vehicleno + "' ");

                this.dispose();

                JOptionPane.showMessageDialog(this, "payment is Confirm.");

                InputStream path = this.getClass().getResourceAsStream("/reports/DeliveryReceipt.jasper");

                HashMap<String, Object> params = new HashMap<>();

                params.put("Parameter1", jTextField1.getText()); //Rental Id
                params.put("Parameter2", jTextField9.getText()); // customer NIC
                params.put("Parameter3", jTextField3.getText());  //Start meter
                params.put("Parameter4", jTextField7.getText()); // Licene Plate
                params.put("Parameter5", jTextField2.getText());  //Start Date
                params.put("Parameter6", jTextField4.getText());  //End meter
                params.put("Parameter7", formatterend.format(endDate));  //End Date
                params.put("Parameter8", jTextField2.getText());  //Driver
                params.put("Parameter9", jTextField6.getText());  //Driver Cost
                params.put("Parameter10", jLabel9.getText());  //Cost Per Km
                params.put("Parameter11", jTextField8.getText());  //Total
                params.put("Parameter12", jTextField5.getText());  //Payment

                //JRTableModelDataSource dataSource = new JRTableModelDataSource(jTable1.getModel());
                JasperPrint jasperPrint = JasperFillManager.fillReport(path, params, new JREmptyDataSource());

                JasperViewer.viewReport(jasperPrint, false);
            }
        } catch (Exception e) {
            e.printStackTrace();
            Logger loggerFile = SplashScreen.getLoggerFile();
            loggerFile.log(Level.SEVERE, "UpdateRent", e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

