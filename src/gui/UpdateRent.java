    private void loadDetail() {
        try {

            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `rental` INNER JOIN `customer` ON"
                    + "`rental`.`customer_nic` = `customer`.`nic` INNER JOIN `vehicle` ON"
                    + "`rental`.`vehicle_licene_no` = `vehicle`.`licene_no`  WHERE `id` = '" + id + "' ");

            while (rs.next()) {
                jTextField1.setText(id);
                jTextField7.setText(rs.getString("licene_no"));
                jTextField2.setText(rs.getString("start_date"));
                jTextField9.setText(rs.getString("customer_nic"));
                SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    jDateChooser1.setDate(formatter.parse(rs.getString("end_date")));
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
                jTextField3.setText(rs.getString("start_meter"));

            }

        } catch (Exception e) {
            e.printStackTrace();
            Logger loggerFile = SplashScreen.getLoggerFile();
            loggerFile.log(Level.SEVERE, "loadRent1", e);
        }
    }


